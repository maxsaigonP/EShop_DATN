using E_Shop.Data;
using E_Shop_DATN.Models;

namespace E_Shop.Areas.Admin.Controllers
{
    [Area("Admin")]
    public class AdminsController : Controller
    {
        private readonly E_ShopContext _context;

        public AdminsController(E_ShopContext context)
        {
            _context = context;
        }

        // GET: Admin/Admins
